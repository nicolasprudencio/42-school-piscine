#include <unistd.h>

void	ft_is_negative(int n)
{
	char	x;
	char	p;

	if (n < 0)
	{
		x = 'N';
		write (1, &x, 1);
	}
	else if (n >= 0)
	{
		p = 'P';
		write (1, &p, 1);
	}
}
