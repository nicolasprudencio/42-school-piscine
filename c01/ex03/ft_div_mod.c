#include <stdio.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int	main(void)
{
	int	a;
	int	b;

	ft_div_mod(5, 2, &a, &b);
	printf("%d \n%d", a, b);
	return (0);
}
