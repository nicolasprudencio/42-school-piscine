int	ft_strncmp(char *s1, char *s2, unsigned int n)
{	
	unsigned int	i;

	i = 0;
	while (i <= n && s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
	{
		if (i == n || s1[i] == '\0' || s2[i] == '\0')
			break ;
		i++;
	}
	if (i == n)
		return (0);
	else
		return (s1[i] - s2[i]);
}
