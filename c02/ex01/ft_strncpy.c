char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

#include <stdio.h>

int main(void)
{
	char str[] = "novo teste";
	char str2[] = "meu novo teste amigo";

	printf("%s\n", ft_strncpy(str, str2, 19));
	return (0);
}